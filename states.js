function toTitleCase(strIn) {
    let first = true;
    let strOut = "";
    for (let i = 0; i < strIn.length; i++) {
        if (first === true) {
            strOut += strIn[i].toUpperCase();
        } else {
            strOut += strIn[i].toLowerCase();
        }
        if (strIn[i] === " " && strIn.slice(i+1, i+3) !== "OF") {
            first = true;
        } else {
            first = false;
        }
    }
    return strOut
}